#include "stdio.h"

/**
 * main - Prints "_putchar" followed by a new line.
 * Return: 0 for success
 */
int main()
{
	int letter;
	putchar("");
	getchar();
	letter = '_';
	putchar(letter);
	letter = 'p';
	putchar(letter);
	letter = 'u';
	putchar(letter);
	letter = 't';
	putchar(letter);
	letter = 'c';
	putchar(letter);
	letter = 'h';
	putchar(letter);
	letter = 'a';
	putchar(letter);
	letter = 'r';
	putchar(letter);
	putchar("\n");
	return (0);
}
